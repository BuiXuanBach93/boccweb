CREATE TABLE
    SHT_ADMIN_USER
    (
        ADMIN_USER_ID NUMBER(11)  NOT NULL,
        ADMIN_ID NUMBER(11)  NOT NULL,        
        USER_ID NUMBER(11),
        CMN_DELETE_FLAG NUMBER(1) DEFAULT 0 NOT NULL, -- 0 - False, 1 - True
        CMN_ENTRY_DATE DATE DEFAULT CURRENT_DATE NOT NULL,
        CMN_LAST_UPDT_DATE DATE DEFAULT CURRENT_DATE NOT NULL,
        CMN_ENTRY_USER_NO NUMBER(11),
        CMN_ENTRY_USER_TYPE NUMBER(1),
        CMN_LAST_UPDT_USER_NO NUMBER(11),
        CMN_LAST_UPDT_USER_TYPE NUMBER(1),
        CONSTRAINT SHT_ADMIN_USER_PK PRIMARY KEY (ADMIN_USER_ID)
    );

CREATE SEQUENCE SHT_ADMIN_USER_SEQ INCREMENT BY 1 START WITH 1 MAXVALUE 9999999999999999999999999999 MINVALUE 1 NOCYCLE CACHE 20 NOORDER;

CREATE INDEX IDX_ADMIN_USER
  ON SHT_ADMIN_USER (ADMIN_ID);
